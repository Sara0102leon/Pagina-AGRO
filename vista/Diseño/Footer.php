            if (open) {
                sidebarMenu.classList.remove('translate-x-full');
                document.body.style.overflow = 'hidden';
            } else {
                sidebarMenu.classList.add('translate-x-full');
                document.body.style.overflow = '';
            }
        }

        menuBtn.addEventListener('click', () => toggleSidebar(true));
        closeBtn.addEventListener('click', () => toggleSidebar(false));
        sidebarOverlay.addEventListener('click', () => toggleSidebar(false));
        sidebarLinks.forEach(link => link.addEventListener('click', () => toggleSidebar(false)));

        // --- CARRUSEL CENTRAL PRINCIPAL ---
        const track = document.getElementById('carrusel-track');
        let indexSlide = 0;
        const totalSlides = track.children.length;

        function moverCarrusel(direccion) {
            indexSlide += direccion;
            if (indexSlide < 0) indexSlide = totalSlides - 1;
            else if (indexSlide >= totalSlides) indexSlide = 0;
            track.style.transform = `translateX(-${indexSlide * 100}%)`;
        }
        setInterval(() => moverCarrusel(1), 3000);

        // --- PUBLICIDAD ASIDE INFINITA ---
        const publicidadSlides = document.querySelectorAll('.ad-slide');
        const publicidadProgressBars = document.querySelectorAll('.ad-progress-bar');
        let currentAdIndex = 0, adTimer = null, animationFrameId = null;
        const AD_DURATION = 1000; 
        let startTimestamp = null, progressPaused = false;

        function inicializarAnuncio(index) {
            cancelAnimationFrame(animationFrameId);
            clearTimeout(adTimer);
            publicidadSlides.forEach((slide, i) => {
                const bounceEl = slide.querySelector('.bounce-element');
                const videoEl = slide.querySelector('video');
                if (i === index) {
                    slide.classList.remove('opacity-0', 'pointer-events-none', 'z-0');
                    slide.classList.add('opacity-100', 'z-10');
                    if (bounceEl) { bounceEl.classList.remove('animate-bounce-in'); void bounceEl.offsetWidth; bounceEl.classList.add('animate-bounce-in'); }
                    if (videoEl) { videoEl.currentTime = 0; videoEl.play().catch(() => {}); }
                } else {
                    slide.classList.remove('opacity-100', 'z-10');
                    slide.classList.add('opacity-0', 'pointer-events-none', 'z-0');
                    if (videoEl) videoEl.pause();
                }
            });
            publicidadProgressBars.forEach((bar, i) => bar.style.width = i < index ? '100%' : '0%');
            currentAdIndex = index;
            startTimestamp = performance.now();
            actualizarLineaProgreso();
            adTimer = setTimeout(() => inicializarAnuncio((currentAdIndex + 1) % publicidadSlides.length), AD_DURATION);
        }

        function actualizarLineaProgreso() {
            if (progressPaused) return;
            const tiempoTranscurrido = performance.now() - startTimestamp;
            const porcentaje = Math.min((tiempoTranscurrido / AD_DURATION) * 100, 100);
            if (publicidadProgressBars[currentAdIndex]) publicidadProgressBars[currentAdIndex].style.width = `${porcentaje}%`;
            if (tiempoTranscurrido < AD_DURATION) animationFrameId = requestAnimationFrame(actualizarLineaProgreso);
        }

        function cambiarAnuncioManualmente(index) { inicializarAnuncio(index); }
